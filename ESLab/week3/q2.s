	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =SRC	;R0 HOLDS ADDRESS OF SRC
	LDR R1, =DST
	MOV R2, #0		;HOLDS SUM
	LDR R3, [R0]
	MLA R2,R3,R3,R3		;MULTIPLIES R3 BY R3 AND THEN ADDS THAT TO R3 AND THEN STORES IT IN R2
	LSR R2, #1       ;TO DIVIDE THE NUMBER BY 2
	STR R2, [R1]     ;STORES CONTENT OF R2 TO ADRESS POINTED BY R1
STOP
	B STOP
SRC DCD 5
	AREA mydata, DATA, READWRITE
DST DCD 0
	END

	