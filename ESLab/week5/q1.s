		AREA RESET, DATA, READONLY
		EXPORT __Vectors		
__Vectors
		DCD 0X10001000
		DCD Reset_Handler
		ALIGN
		AREA mycode, CODE, READONLY
		ENTRY
		EXPORT Reset_Handler
Reset_Handler
		;WRITE INTO DATA MEMORY FROM CODE
		LDR R0,=ARR
		LDR R1,=DRR
		MOV R2,#5
STORE 	LDR R3,[R0],#4
		STR R3,[R1],#1
		SUBS R2,#1
		BNE STORE
		LDR R0,=DRR
		MOV R1,R0
		MOV R3,#5
L1		MOV R5,R1
		LDRB R4,[R1],#1
		MOV R2,R1
		SUBS R3,#1
		MOV R7,R3
L2		LDRB R6,[R2]
		CMP R6,R4
		BCC UPDATE
		B CONT
UPDATE	MOV R5,R2
CONT	ADD R2,#1
		SUBS R7,#1
		BNE L2
		LDR R6,[R5]
		STRB R6,[R1,#-1]
		STRB R4,[R5]
		CMP R3,#1
		BNE L1	
STOP	B STOP
ARR DCD 3,2,1,5,4
	AREA mydata, DATA, READWRITE
DRR DCD 0
		END